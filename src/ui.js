  async fillByKey(key){
    console.log('fillByKey',key);
    let cacheUrl = localStorage.getItem('imageCache'+key)
    if(cacheUrl){
      this.fillImage(cacheUrl)
    }else{
      // getByCos
      let url = await window.getObjectUrl(key)
      localStorage.setItem('imageCache'+key,url)
      this.fillImage(url)
    }
  }
  /**
   * Shows an image
   *
   * @param {string} url - image source
   * @returns {void}
   */
  fillImage(url) {
    /**
     * Check for a source extension to compose element correctly: video tag for mp4, img — for others
     */
    // const tag = /\.mp4$/.test(url) ? 'VIDEO' : 'IMG';
    let tag = 'IMG';

    if (url.includes(".mp4") || url.includes(".avi") || url.includes(".mov")||
      url.includes(".MP4") || url.includes(".AVI") || url.includes(".MOV")) {
      tag = "VIDEO";
    } else{
      tag = "IMG";
    }

    // if (url.includes("mp4")) {
    //   tag = 'VIDEO'
    //   // console.log("The URL contains 'mp4'");
    // }

    // console.log('fillImage',url,tag);

    const attributes = {
      src: url,
    };

    /**
     * We use eventName variable because IMG and VIDEO tags have different event to be called on source load
     * - IMG: load
     * - VIDEO: loadeddata
     *
     * @type {string}
     */
    let eventName = 'load';

    /**
     * Update attributes and eventName if source is a mp4 video
     */
    if (tag === 'VIDEO') {
      /**
       * Add attributes for playing muted mp4 as a gif
       *
       * @type {boolean}
       */
      attributes.autoplay = true;
      attributes.loop = true;
      attributes.muted = true;
      attributes.playsinline = true;
      attributes.controls = true;

      /**
       * Change event to be listened
       *
       * @type {string}
       */
      eventName = 'loadeddata';
    }

    /**
     * Compose tag with defined attributes
     *
     * @type {Element}
     */
    this.nodes.imageEl = make(tag, this.CSS.imageEl, attributes);
    console.log('omComplete',this);
    

    /**
     * Add load event listener
     */
    this.nodes.imageEl.addEventListener(eventName, () => {
      this.toggleStatus(Ui.status.FILLED);

      /**
       * Preloader does not exists on first rendering with presaved data
       */
      if (this.nodes.imagePreloader) {
        this.nodes.imagePreloader.style.backgroundImage = '';
      }
      if(this.config.onComplete){
        this.config.onComplete()
      }
    });

    this.nodes.imageContainer.appendChild(this.nodes.imageEl);
  }

